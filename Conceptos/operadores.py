# OPERADORES RELACIONALES
variable_uno = 10
variable_dos = 18

mayor = variable_uno > variable_dos
menor = variable_uno < variable_dos
mayor_igual = variable_uno >= variable_dos
menor_igual = variable_uno <= variable_dos
igual = variable_uno == variable_dos  # Tambien podemos usar la palabra is 
diferente = variable_uno != variable_dos

print (mayor)
print (menor)
print (mayor_igual)
print (menor_igual)
print (igual)
print (diferente)

# Operador AND
resultadoAnd = True and False # Para que sea True las 2 comparaciones debe ser True
print(resultadoAnd)


# Operador OR
resultadoOr = True or False # Para que sea True una de ellas debe ser True
print(resultadoOr)

# Operador NOT
resultadoNot = not False # True
resultadoNot = not True  # False
print(resultadoNot)


